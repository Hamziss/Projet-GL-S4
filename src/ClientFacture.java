    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
          String email = Login.getemail();
          int numC;
          
        ResultSet rs1 = Select.getData("select * from personne where email like '%"+email+"%'"); 
         DefaultTableModel model= (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        
        try {
            if(rs1.next())
            {
            numC = rs1.getInt(1);
            ResultSet rs2 = Select.getData("select * from commande where NumC='"+numC+"'");
            while(rs2.next() ){
                model.addRow(new Object[]{rs2.getString(3),rs2.getString(6),rs2.getString(8),rs2.getString(9)});
            }
            rs1.close();
            rs2.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(ClientFacture.class.getName()).log(Level.SEVERE, null, ex);
        }
          
      
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

